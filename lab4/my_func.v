module func_assign(x1, x2, x3, f);
    input x1, x2, x3;
    output f;
    assign f = (~x3 & ~x2 & ~x1) | (~x3 & x2 & x1) | (x3 & ~x2 & x1);
endmodule

// ????? 2: ??????????? ???? ?? ??????? ?????????
module func_struct(x1, x2, x3, f);
    input x1, x2, x3;
    output f;
    wire nx1, nx2, nx3;
    wire and1, and2, and3;
    
    not (nx1, x1);
    not (nx2, x2);
    not (nx3, x3);
    
    and (and1, nx3, nx2, nx1);
    and (and2, nx3, x2, x1);
    and (and3, x3, nx2, x1);
    
    or (f, and1, and2, and3);
endmodule

